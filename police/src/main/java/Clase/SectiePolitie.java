package Clase;

public class SectiePolitie {
	String NumeSectie;
	String AdresaSectie;
	String SpecificSectie;
	String Nume_IP;
	
	public String getNumeSectie() {
		return NumeSectie;
	}
	public void setNumeSectie(String numeSectie) {
		NumeSectie = numeSectie;
	}
	public String getAdresaSectie() {
		return AdresaSectie;
	}
	public void setAdresaSectie(String adresaSectie) {
		AdresaSectie = adresaSectie;
	}
	public String getSpecificPolitie() {
		return SpecificSectie;
	}
	public void setSpecificSectie(String specificSectie) {
		SpecificSectie = specificSectie;
	}
	public String getNume_IP() {
		return Nume_IP;
	}
	public void setNume_IP(String nume_IP) {
		Nume_IP = nume_IP;
	}
}
